package com.vk2.touchsreentab.model.viewmodel;

public class FileObject {
    private String name;
    private String path;
    private int type;

    public FileObject(String name, String path, int type) {
        this.name = name;
        this.path = path;
        this.type = type;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }

}
